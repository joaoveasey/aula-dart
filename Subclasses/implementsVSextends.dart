// Diferenças entre extends e implements

// extends:

// - Usado para criar uma subclasse que herda características de uma classe pai.

// - A subclasse pode usar métodos e propriedades da classe pai, além de poder sobrescrever métodos.

// -----------------

// implements:

// - Usado para "implementar" uma interface (que pode ser uma classe abstrata).

// - A classe que implementa deve fornecer suas próprias implementações para todos os métodos e propriedades da interface.

// - Não herda o comportamento da classe; em vez disso, precisa definir tudo o que é declarado.

// -----------------

// RESUMO:

// - extends é usado para herança, permitindo que a subclasse herde métodos e propriedades.

// - implements é usado para definir uma classe que deve implementar todos os métodos e propriedades de uma interface, garantindo que ela cumpra um contrato específico.